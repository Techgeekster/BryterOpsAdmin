import * as $ from 'jquery';
import 'jquery-popup-overlay';

import { Directive, ElementRef, Input } from '@angular/core';

@Directive({ selector: '[bryterOverlay]' })
export class BryterOverlayDirective {
    constructor(el: ElementRef) {
        console.log("Start BryterOverlayDirective constructor");
        $(el.nativeElement).text("Directive!");
        ($(el.nativeElement) as any).popup();
        console.log("End BryterOverlayDirective constructor");
    }
}